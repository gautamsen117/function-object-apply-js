const grade = 'A+';
switch(grade){
    case 'A+':
        console.log('Good Job');
        break;
    case 'A':
        console.log('You got A grade');
        break;
    case 'B':
        console.log('Your grade is B');
        break;
    case 'F':
        console.log('Fail this exam');
        break;
    default:
        console.log('Your result is not find');
        break;
}